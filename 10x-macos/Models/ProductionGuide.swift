            modules: [
                ProductionGuideModule(
                    id: "generated-app",
                    title: "What You Already Have",
                    summary: "\(projectName) currently has \(sourceCount) generated source file\(sourceCount == 1 ? "" : "s"). Treat this project as the product surface and app shell, not the trust boundary.",
                    bullets: [
