        projectName: String,
        sourceCount: Int,
        secureKeys: [String],
        plainKeys: [String]
    ) -> ProductionGuideSection {
        ProductionGuideSection(
            id: "foundation",
            title: "Production Foundation",
            summary: "Start by being explicit about what the shipped app should do and what should move behind infrastructure.",
