enum ProductionGuideBuilder {
    static let generatedMarker = "<!-- Generated by 10x -->"

    static func build(
        projectName: String,
        fileTree: [String: String],
        environmentVariables: [ProjectEnvironmentVariable]
    ) -> ProductionGuide {
        let secureKeys = ProjectEnvironmentSecurity.sensitiveKeys(from: environmentVariables)
        let plainKeys = ProjectEnvironmentSecurity.plainKeys(from: environmentVariables)
        let sourceCount = fileTree.count

        let summary = """
        \(projectName) already gives you the iOS client. To make it production-ready, keep the app thin, move privileged work behind infrastructure you control, and validate the real backend before you ship.
        """

        return ProductionGuide(
            title: "Publishing Guide",
            summary: summary,
            sections: [
                foundationSection(
                    projectName: projectName,
                    sourceCount: sourceCount,
                    secureKeys: secureKeys,
                    plainKeys: plainKeys
                ),
                backendStackSection(),
                hardeningSection(),
                releaseSection(secureKeys: secureKeys, plainKeys: plainKeys),
            ],
            generatedAt: Date()
        )
    }

