import Foundation

struct ProductionGuide: Sendable, Equatable {
    let title: String
    let summary: String
    let sections: [ProductionGuideSection]
    let generatedAt: Date

    nonisolated var markdown: String {
        let timestamp = ISO8601DateFormatter().string(from: generatedAt)
        let sectionText = sections.map(\.markdown).joined(separator: "\n\n")
        return """
        \(ProductionGuideBuilder.generatedMarker)
        # PRODUCTION.md

        ## \(title)

        \(summary)

        Generated by 10x on \(timestamp).

        \(sectionText)
        """
    }
}

struct ProductionGuideSection: Identifiable, Sendable, Equatable {
    let id: String
    let title: String
    let summary: String
    let icon: String
    let modules: [ProductionGuideModule]

    nonisolated var markdown: String {
        let modulesText = modules.map(\.markdown).joined(separator: "\n\n")
        return """
        ## \(title)

        \(summary)

        \(modulesText)
        """
    }
}

struct ProductionGuideModule: Identifiable, Sendable, Equatable {
    let id: String
    let title: String
    let summary: String
    let bullets: [String]
    let codeBlockLanguage: String?
    let codeBlock: String?

    nonisolated var markdown: String {
        let bulletText = bullets.map { "- \($0)" }.joined(separator: "\n")
        let codeText: String
        if let codeBlock, !codeBlock.isEmpty {
            let language = codeBlockLanguage ?? ""
            codeText = "\n\n```\(language)\n\(codeBlock)\n```"
        } else {
            codeText = ""
        }

        if bulletText.isEmpty {
            return """
            ### \(title)

            \(summary)\(codeText)
            """
        }

        return """
        ### \(title)

        \(summary)

        \(bulletText)\(codeText)
        """
    }
}

struct ProductionChecklistState: Codable, Sendable, Equatable {
    var checkedItemIDs: Set<String>
    var updatedAt: String

    init(
        checkedItemIDs: Set<String> = [],
        updatedAt: String = Self.timestamp()
    ) {
        self.checkedItemIDs = checkedItemIDs
        self.updatedAt = updatedAt
    }

    func isChecked(_ itemID: String) -> Bool {
        checkedItemIDs.contains(itemID)
    }

    mutating func setChecked(_ isChecked: Bool, for itemID: String) {
        if isChecked {
            checkedItemIDs.insert(itemID)
        } else {
            checkedItemIDs.remove(itemID)
        }
        updatedAt = Self.timestamp()
    }

    private static func timestamp() -> String {
        ISO8601DateFormatter().string(from: Date())
    }

    nonisolated static let empty = Self()
}

